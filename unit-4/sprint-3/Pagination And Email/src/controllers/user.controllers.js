const express = require('express');
const User = require('../models/user.model');
const transporter = require('../config/mail');
const path = require('path');
const router = express.Router();

const generateOTP = () => {
    var digits = '0123456789';
    let OTP = '';
    for (let i = 0; i < 6; i++) {
        OTP += digits[Math.floor(Math.random() * 10)];
    }
    return OTP;
}

router.get('/', async (req, res) => {
    try {
        const page = +req.query.page || 1;
        const pagesize = +req.query.pagesize || 10;
        const skip = (page - 1) * pagesize;
        const users = await User.find().skip(skip).limit(pagesize).lean().exec();
        const totalpage = Math.ceil((await User.find().countDocuments()) / pagesize);
        console.log(totalpage);
        return res.status(200).send({ users: users });
    } catch (error) {
        return res.status(500).send(error);
    }
});

router.post('/', async (req, res) => {
    try {
        const otp = generateOTP();
        const user = await User.create(req.body);
        transporter.sendMail({
            from: '"amazom@exapmle.com" <admin@amazon.com>', // sender address
            to: user.email, // list of receivers
            subject: "OTP generated by Vedix.com.", // Subject line
            text: `Please enter generated OTP ${otp} to confirm your order.`, // plain text body
            //html: "<b>Hello sir/ma'am, your product is successfully created.</b>", // html body

            alternatives: [
                {
                    contentType: 'text/html',
                    path: path.join(__dirname, '../user.mail.html')
                },
                {
                    filename: 'file.text',
                    path: path.join(__dirname, "../mails/file.text")
                }
            ]
        });
        return res.status(201).send({ message: 'Product created successfully' });

    } catch (error) {
        return res.status(500).send(error);
    }

});

module.exports = router;